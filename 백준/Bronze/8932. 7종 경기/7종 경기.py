s = {0:[9.23076, 26.7, 1.835, 't'],
     1:[1.84523, 75, 1.348, 'f'],
     2:[56.0211, 1.5, 1.05, 'f'],
     3:[4.99087, 42.5, 1.81, 't'],
     4:[0.188807, 210, 1.41, 'f'],
     5:[15.9803, 3.8, 1.04, 'f'],
     6:[0.11193, 254, 1.88, 't']}
for _ in range(int(input())):
  p = list(map(int, input().split()))
  h = 0
  for i, v in enumerate(p):
    if s[i][-1]=='t':
      h += int(s[i][0]*(s[i][1]-v)**s[i][2])
    else:
      h += int(s[i][0]*(v-s[i][1])**s[i][2])
  print(h)